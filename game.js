function handleCanvasClick(event) {
  const point = canvasPoint(event);
  const chest = chests.find(item => point.x >= item.x - 8 && point.x <= item.x + 30 && point.y >= item.y - 10 && point.y <= item.y + 34);
  if (chest) {
    openChest(chest);
    return;
  }

  if (point.x >= 650 && point.x <= 810 && point.y >= 130 && point.y <= 250) {
    showToast("农舍里好像有什么，靠近后多按几次空格试试。");
    return;
  }

  const hitTree = trees.some(([x, y]) => point.x >= x && point.x <= x + 60 && point.y >= y && point.y <= y + 92);
  if (hitTree) {
    treeClicks += 1;
    if (treeClicks >= 4 && !state.eggTree) {
      state.eggTree = true;
      saveState();
      showToast("树叶里藏着一句话：新的一岁，也要被温柔偏爱。");
    }
    return;
  }

  showToast("用方向键或 WASD 一格一格移动，靠近礼物后按空格打开。");
}

function handleKey(event) {
  const key = event.key.toLowerCase();
  if (event.key === "Escape") {
    memoryModal.classList.add("hidden");
    letterModal.classList.add("hidden");
    return;
  }
  if (!memoryModal.classList.contains("hidden") || !letterModal.classList.contains("hidden")) return;
  const isMoving = Math.abs(player.x - player.targetX) + Math.abs(player.y - player.targetY) > 1;
  if (["arrowup", "arrowdown", "arrowleft", "arrowright", "w", "a", "s", "d", " ", "enter"].includes(key)) {
    event.preventDefault();
  }
  if (key === "enter" || key === " ") {
    const nearest = chests.find(chest => !chest.open && isPlayerNearGift(chest));
    if (nearest) openChest(nearest);
    else if (handleHouseEasterEgg()) return;
    else showToast("附近没有可以打开的礼物。");
    return;
  }
  if (!directionKeys.includes(key)) return;
  heldKeys.add(key);
  if (isMoving) return;
  const direction = getHeldDirection();
  if (direction) stepPlayer(direction.x, direction.y);
}

