function createAudio() {
  const AudioContext = window.AudioContext || window.webkitAudioContext;
  const context = new AudioContext();
  const master = context.createGain();
  const delay = context.createDelay();
  const feedback = context.createGain();
  master.gain.value = 0.075;
  delay.delayTime.value = 0.18;
  feedback.gain.value = 0.18;
  master.connect(context.destination);
  master.connect(delay);
  delay.connect(feedback);
  feedback.connect(delay);
  delay.connect(context.destination);
  const notes = [
    392, 392, 440, 392, 523.25, 493.88,
    392, 392, 440, 392, 587.33, 523.25,
    392, 392, 783.99, 659.25, 523.25, 493.88, 440,
    698.46, 698.46, 659.25, 523.25, 587.33, 523.25
  ];
  const beats = [
    1, 1, 2, 2, 2, 4,
    1, 1, 2, 2, 2, 4,
    1, 1, 2, 2, 2, 2, 4,
    1, 1, 2, 2, 2, 5
  ];
  let step = 0;
  let timer = null;

  function playNote(freq, beat) {
    const now = context.currentTime;
    const osc = context.createOscillator();
    const shimmer = context.createOscillator();
    const noteGain = context.createGain();
    const filter = context.createBiquadFilter();
    osc.type = "sine";
    shimmer.type = "triangle";
    osc.frequency.value = freq;
    shimmer.frequency.value = freq * 2;
    filter.type = "highpass";
    filter.frequency.value = 450;
    noteGain.gain.setValueAtTime(0.0001, now);
    noteGain.gain.linearRampToValueAtTime(0.52, now + 0.018);
    noteGain.gain.exponentialRampToValueAtTime(0.0001, now + 0.18 + beat * 0.15);
    osc.connect(filter);
    shimmer.connect(filter);
    filter.connect(noteGain).connect(master);
    osc.start(now);
    shimmer.start(now);
    osc.stop(now + 0.22 + beat * 0.15);
    shimmer.stop(now + 0.22 + beat * 0.15);
  }

  return {
    async start() {
      await context.resume();
      if (timer) return;
      timer = setInterval(() => {
        const beat = beats[step % beats.length];
        playNote(notes[step % notes.length], beat);
        if (step % 6 === 0) playNote(notes[step % notes.length] / 2, 2);
        step += 1;
      }, 330);
    },
    stop() {
      clearInterval(timer);
      timer = null;
    }
  };
}

